export const getCardImageIndex = (cardId: string): number => {
  // Simple hash to get a consistent index for each card
  let hash = 0;
  for (let i = 0; i < cardId.length; i++) {
    hash = cardId.charCodeAt(i) + ((hash << 5) - hash);
  }
  return Math.abs(hash) % TROPICAL_PLANT_IMAGES.length;
};
